package com.etaoguan.wea.e688.vo;

public class ProductDiffer {
	private long offerId;
	private long differId;
	private String differName;
	private Double differPrice;
	private Integer defaultFlag;
	public long getOfferId() {
		return offerId;
	}
	public void setOfferId(long offerId) {
		this.offerId = offerId;
	}
	public long getDifferId() {
		return differId;
	}
	public void setDifferId(long differId) {
		this.differId = differId;
	}
	public String getDifferName() {
		return differName;
	}
	public void setDifferName(String differName) {
		this.differName = differName;
	}
	public Double getDifferPrice() {
		return differPrice;
	}
	public void setDifferPrice(Double differPrice) {
		this.differPrice = differPrice;
	}
	public Integer getDefaultFlag() {
		return defaultFlag;
	}
	public void setDefaultFlag(Integer defaultFlag) {
		this.defaultFlag = defaultFlag;
	}

}
