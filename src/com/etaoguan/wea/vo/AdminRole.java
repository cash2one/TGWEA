package com.etaoguan.wea.vo;

import java.util.List;

public class AdminRole extends BaseVo{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int roleId;
	private String roleName;
	private String remark;
	private List<AccessOperation> accessFuncList;
	public int getRoleId() {
		return roleId;
	}
	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public List<AccessOperation> getAccessFuncList() {
		return accessFuncList;
	}
	public void setAccessFuncList(List<AccessOperation> accessFuncList) {
		this.accessFuncList = accessFuncList;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
