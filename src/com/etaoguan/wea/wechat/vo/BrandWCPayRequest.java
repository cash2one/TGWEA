package com.etaoguan.wea.wechat.vo;

public class BrandWCPayRequest {

	private String appid;
	private String timeStamp;
	private String nonceStr;
	private String packageStr;
	private String paySign;
	private String realPriceTotal;
	
	private String status = "S";
	private String errorMessage;
	
	public String getRealPriceTotal() {
		return realPriceTotal;
	}


	public void setRealPriceTotal(String realPriceTotal) {
		this.realPriceTotal = realPriceTotal;
	}


	public String getAppid() {
		return appid;
	}


	public void setAppid(String appid) {
		this.appid = appid;
	}


	public String getTimeStamp() {
		return timeStamp;
	}


	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}


	public String getNonceStr() {
		return nonceStr;
	}


	public void setNonceStr(String nonceStr) {
		this.nonceStr = nonceStr;
	}


	public String getPackageStr() {
		return packageStr;
	}


	public void setPackageStr(String packageStr) {
		this.packageStr = packageStr;
	}



	public String getPaySign() {
		return paySign;
	}


	public void setPaySign(String paySign) {
		this.paySign = paySign;
	}


	public String getStatus() {
		return status;
	}


	public void setStatus(String status) {
		this.status = status;
	}


	public String getErrorMessage() {
		return errorMessage;
	}


	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	
	
}
